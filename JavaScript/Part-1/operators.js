//Arithmetic operators
let a=10;
let b=5;
console.log(a+b);  //15
console.log(a-b);  //5
console.log(a*b);  //50
console.log(a/b);  //2
console.log(a%b);  //0
console.log(a**b); //1000

//unary
console.log(a++); //10
console.log(++a); //12


//Assignment
console.log(a=b);  //5
console.log(a+=b);  //10
console.log(a*=b);  //50


